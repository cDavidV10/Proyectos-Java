package Funciones;

/**
 *
 * @author cdavi
 */
public class Tareas {
    private String id;
    private String info;
    private String estado;


    public Tareas(String id, String info, String estado) {
        this.id = id;
        this.info = info;
        this.estado = estado;
    }
    
    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
